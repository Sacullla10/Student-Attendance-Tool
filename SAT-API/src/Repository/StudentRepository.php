    }

    public function findAttendancesBySession(int $classSessionId): array
    {
        return $this->createQueryBuilder('s')
        ->leftJoin('s.attendances', 'a', 'WITH', 'a.class_session = :sessionId')
        ->addSelect('a')
        ->setParameter('sessionId', $classSessionId)
        ->getQuery()
        ->getResult();
    }

